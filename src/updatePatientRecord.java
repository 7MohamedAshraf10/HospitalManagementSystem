    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
            
        String patientID = jTextField4.getText();
        String name = jTextField7.getText();
        String contactNumber = jTextField2.getText();
        String age = jTextField1.getText();
        String gender = jTextField8.getText();
        String bloodGroup = jTextField3.getText();
        String address =jTextField6.getText();
        String anyMajorDisease = jTextField5.getText();
        
        try{
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            st.executeUpdate(" Update patient set name = '"+name+" ', contactNumber =' "+contactNumber+"', age ='"+age+" ',gender =' "+gender+"',bloodgroup ='" +bloodGroup+"', address ='"+address+"', anyMajorDisease = '"+anyMajorDisease+"' where patientID = '"+patientID+"'" );
            JOptionPane.showMessageDialog(null, "Successfully Updated! ");
            setVisible(false);
            new updatePatientRecord().setVisible(true);
        }
        catch(Exception e)
        {
             JOptionPane.showMessageDialog(null, "Please enter the data in correct formate! ");
        }
        
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

