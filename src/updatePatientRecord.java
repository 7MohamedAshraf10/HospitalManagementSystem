    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String patientID = jTextField4.getText();

        try {
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from patient where patientID =  '" + patientID + "' ");

            if (rs.next()) {
                jTextField7.setText(rs.getString(2));
                jTextField2.setText(rs.getString(3));
                jTextField1.setText(rs.getString(4));
                jTextField8.setText(rs.getString(5));
                jTextField3.setText(rs.getString(6));
                jTextField6.setText(rs.getString(7));
                jTextField5.setText(rs.getString(8));
                
                jTextField4.setEditable(false);

            }
            else
            {
                JOptionPane.showMessageDialog(null, "Patient ID does not exsit! ");
            }

        } catch (Exception e) {

        }

    }//GEN-LAST:event_jButton1ActionPerformed

